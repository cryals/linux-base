function adjustWeightsBasedOnAnswers() {
  const weights = [...dynamicWeights.value];

  // Если новичок — понижаем сложные дистрибутивы, повышаем простые
  if (userAnswers.value[0] === 0) { // Опыт: новичок
    weights[5] *= 0.5; // Arch Linux
    weights[7] *= 0.8; // OpenSUSE
    weights[6] *= 1.3; // Linux Mint
    weights[0] *= 1.2; // Ubuntu
  }

  // Если эксперт — повышаем Arch, Fedora, OpenSUSE
  if (userAnswers.value[0] === 3) {
    weights[5] *= 1.5; // Arch
    weights[8] *= 1.3; // Fedora
    weights[7] *= 1.2; // OpenSUSE
  }

  // Если NVIDIA — повышаем Pop!_OS и Ubuntu
  if (userAnswers.value[10] === 0) { // Видеокарта: NVIDIA
    weights[4] *= 1.5; // Pop!_OS
    weights[0] *= 1.2; // Ubuntu
    weights[9] *= 1.1; // Manjaro
  }

  // Если AMD — нейтрально, но немного Manjaro/Fedora
  if (userAnswers.value[10] === 1) {
    weights[9] *= 1.1;
    weights[8] *= 1.1;
  }

  // Если мало RAM (<4GB) — повышаем Debian, ALT, понижаем тяжелые
  if (userAnswers.value[11] === 0) {
    weights[1] *= 1.3; // Debian
    weights[2] *= 1.2; // ALT
    weights[4] *= 0.7; // Pop!_OS
    weights[8] *= 0.8; // Fedora
  }

  // Если основная цель — игры
  if (userAnswers.value[20] === 2) {
    weights[4] *= 1.5; // Pop!_OS
    weights[9] *= 1.3; // Manjaro
    weights[0] *= 1.2; // Ubuntu
  }

  // Если основная цель — программирование
  if (userAnswers.value[20] === 1) {
    weights[5] *= 1.3; // Arch
    weights[8] *= 1.2; // Fedora
    weights[7] *= 1.1; // OpenSUSE
  }

  // Если важна русская локализация
  if (userAnswers.value[30] === 0) {
    weights[2] *= 1.4; // ALT Linux
    weights[3] *= 1.4; // RedOS
    weights[6] *= 1.1; // Linux Mint
  }

  // Если нужна максимальная стабильность
  if (userAnswers.value[40] === 0) {
    weights[1] *= 1.5; // Debian
    weights[0] *= 1.2; // Ubuntu LTS
    weights[7] *= 1.2; // OpenSUSE Leap
    weights[5] *= 0.5;  // Arch
  }

  // Если нужны самые свежие пакеты
  if (userAnswers.value[41] === 3) {
    weights[5] *= 1.5; // Arch
    weights[8] *= 1.3; // Fedora
    weights[9] *= 1.2; // Manjaro
    weights[1] *= 0.6;  // Debian stable
  }

  // Если важна безопасность (госсектор, корпорации)
  if (userAnswers.value[50] === 0 || userAnswers.value[51] === 0) {
    weights[3] *= 1.6; // RedOS
    weights[1] *= 1.2; // Debian
    weights[7] *= 1.2; // OpenSUSE
    weights[8] *= 1.1; // Fedora (SELinux)
  }

  // Если нужна поддержка игр/Steam
  if (userAnswers.value[80] === 0) {
    weights[4] *= 1.4; // Pop!_OS
    weights[9] *= 1.3; // Manjaro
    weights[0] *= 1.2; // Ubuntu
  }

  // Если нужна поддержка Docker/Kubernetes
  if (userAnswers.value[90] === 0) {
    weights[8] *= 1.3; // Fedora
    weights[0] *= 1.2; // Ubuntu
    weights[7] *= 1.2; // OpenSUSE
  }

  dynamicWeights.value = weights;
}

