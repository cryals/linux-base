const scores = computed(() => {
  adjustWeightsBasedOnAnswers();
  const result = [...initialScores];
  userAnswers.value.forEach((answer, qIndex) => {
    if (answer !== null && questions[qIndex] && questions[qIndex].options[answer]) {
      questions[qIndex].options[answer].scores.forEach((score, dIndex) => {
        result[dIndex] += score * dynamicWeights.value[dIndex];
      });
    }
  });
  return result;
});

// ========== СОРТИРОВКА И ТОП-3 ==========
const sortedDistros = computed(() => {
  return des
    .map((distro, index) => ({
      name: distro.name,
      desc: distro.desc,
      pros: distro.pros,
      cons: distro.cons,
      score: scores.value[index]
    }))
    .sort((a, b) => b.score - a.score);
});


const topDistros = computed(() => {
  return sortedDistros.value.slice(0, 3);
});


