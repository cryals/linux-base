const scores = computed(() => {
  const result = [...initialScores]; // Начальные баллы
  userAnswers.value.forEach((answer, index) => {
    if (answer !== null) {
      questions[index].options[answer].scores.forEach((score, i) => {
        result[i] += score;
      });
    }
  });
  return result;
});

// Определение победителя
const sortedDes = computed(() => {
  return des
    .map((de, index) => ({ name: de, score: scores.value[index] }))
    .sort((a, b) => b.score - a.score);
});

// Топ-1 или топ-2 рабочих сред
const topDes = computed(() => {
  const top = sortedDes.value.filter((de, index, array) => de.score === array[0].score);
  return top.slice(0, 2); // Возвращаем максимум две рабочие среды
});

